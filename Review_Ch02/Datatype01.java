package Review_Ch02;

public class Datatype01 {

	public static void main(String[] args) {
		int a = 10;//int형 변수 a 선언과 초기화
		short s =2;//short형 변수 s 선언과 초기화
		byte b = 6;//byte형 변수 b 선언과 초기화
		long l = 125362133223L;//long형 변수 ㅣ 선언과 초기화(값 뒤에 L써줘야됨)
		
		System.out.println(a);//선언된 변수값들 출력
		System.out.println(s);
		System.out.println(b);
		System.out.println(l);
		

	}

}

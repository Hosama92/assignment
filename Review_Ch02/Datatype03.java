package Review_Ch02;

public class Datatype03 {

	public static void main(String[] args) {
		char ch = 'J';//문자 자료형 변수 ch에 값 J를 넣어줌
		char ch2 = 'a';
		char ch3 = 'v';
		char ch4 = 'a';
		System.out.println(ch);//ch를 출력하고 줄바꿈
		System.out.println(ch2);
		System.out.println(ch3);
		System.out.println(ch4);
		//Java를 세로로 한글자씩 출력
	}
}

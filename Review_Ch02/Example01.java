package Review_Ch02;

public class Example01 {

	public static void main(String[] args) {
		int days = 10;
		//int형 변수 days를 선언과 동시에 초기화 
		System.out.println(days);
		//변수 days의 값을 출력하고 줄바꿈
		int speed;
		//int형 변수 speed를 선언
		speed = 20;
		//speed를 20으로 초기화
		System.out.println(speed);
		//speed의 값을 출력하고 줄바꿈

	}

}

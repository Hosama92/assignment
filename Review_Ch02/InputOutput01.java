package Review_Ch02;

public class InputOutput01 {

	public static void main(String[] args) {
		
		String myName = "홍길동";//문자열 변수 myName에 값 홍길동 대입
		int myAge = 20;//정수형 변수 myAge에 값 20 대입
		
		System.out.println("이름 : "+myName);//문자열 "이름 :"과 변수myName을 +로 결합
		System.out.println("나이 : "+myAge);
	}

}

package Review_Ch03;

public class Example07 {

	public static void main(String[] args) {
		double d = 3.14;
		// 실수형 변수 d를 3.14로 초기화
		long l = (long)d;
		// 변수 d를 정수형 l로 캐스팅 형변환 > 정수가 되면서 소수점은 버림  l=3
		int i = (int)l;// 변수 l을 int형으로 캐스팅 i=3
		System.out.println(d);
		System.out.println(l);
		System.out.println(i);
		
		


	}

}

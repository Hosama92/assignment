package Review_Ch03;

public class Operator01 {

	public static void main(String[] args) {
		int a = 10;
		double b = 2.0;
		System.out.println("a + b = " + (a + b));
		//b의 값이 실수형이므로 결과값도 실수형으로 나온다(소수점 출력)
		//a + b값 출력
		System.out.println("a - b = " + (a - b));
		//a - b 출력
		System.out.println("a * b = " + (a * b));
		//a * b 출력
		System.out.println("a / b = " + (a / b));
		//a / b 출력
		System.out.println("a % b = " + (a % b));
		//a % b 출력
	}
}

package Review_Ch03;

public class Example04 {

	public static void main(String[] args) {
		int x = 10;
		System.out.println(x);
		//초기 x값 출력 ==> x = 10
		x += 2;//x를 x+2로 초기화시켜줌
		System.out.println(x);
		//x값은 10+2=12
		x *= 2;//x를 x*2로 초기화
		System.out.println(x);
		//x값은 12*2=24
		x -= 2;//x를 x-2로 초기화
		System.out.println(x);
		//x값은 24-2=22
	}

}

package Review_Ch03;

public class Example01 {

	public static void main(String[] args) {
		int x = 10, y = 20;
		//int변수 각각 10 20 으로 초기화
		System.out.println("x + y = " + (x + y));
		//x + y = 문자열과 x + y값 출력
		System.out.println("x - y = " + (x - y));
		// x y의 차이값 출력
		System.out.println("x * y = " + (x * y));
		//x y의 곱한값 출력
		System.out.println("x / y = " + (x / y));
		//x 나누기 y 값(몫) 출력
		System.out.println("x % y = " + (x % y));
		//x 나누기 y의 나머지 출력
	}
}

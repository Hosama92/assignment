package Review_Ch04;

public class Example04 {

	public static void main(String[] args) {
		int num = 70;// num에 70할당
		if (num <100) {// num이 100보다 작을때
			System.out.println("숫자가 100 보다 작습니다.");// 숫자가 100 보다 작습니다 출력
			if(num > 50) {// 앞의 if문이 참이고 num이 50보다 클때
				System.out.println("숫자가 50보다 큽니다.");// 숫자가 50보다 큽니다. 출력
			}
		}


	}

}
